Harley-Davidson, Inc. (H-D), or Harley, is an American motorcycle manufacturer, founded in Milwaukee, Wisconsin in 1903.

One of two major American motorcycle manufacturers to survive the Great Depression (along with Indian),[2] the company has survived numerous ownership arrangements, subsidiary arrangements (e.g., Aermacchi 1960-1978 and Buell 1987-2009), periods of poor economic health and product quality, as well as intense global competition,[3] to become one of the world's largest motorcycle manufacturers and an iconic brand widely known for its loyal following. There are owner clubs and events worldwide as well as a company-sponsored brand-focused museum.

Noted for a style of customization that gave rise to the chopper motorcycle style,[4] Harley-Davidson traditionally marketed heavyweight, air-cooled cruiser motorcycles with engine displacements greater than 700 cmÂ³ and has broadened its offerings to include its more contemporary VRSC (2002) and middle-weight Street (2015) platforms.

Harley-Davidson manufactures its motorcycles at factories in York, Pennsylvania; Milwaukee, Wisconsin; Kansas City, Missouri (closing); Manaus, Brazil; and Bawal, India. Construction of a new plant in Thailand is scheduled to begin in late 2018.[5] The company markets its products worldwide.

Besides motorcycles, the company licenses and markets merchandise under the Harley-Davidson brand, among them apparel, home decor and ornaments, accessories, toys, and scale figures of its motorcycles, and video games based on its motorcycle line and the community.

